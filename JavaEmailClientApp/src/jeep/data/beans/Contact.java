package jeep.data.beans;

/**
 * This is a java bean for the database Contact table.
 * 
 * @author Natacha Gabbamonte 0932340
 * 
 */
public class Contact {
	private int contactId;
	private String firstName;
	private String lastName;
	private String email;
	private String phoneNumber;
	private String address;
	private String comments;

	/**
	 * Constructor. Initializes variables to "" and contact id to -1.
	 */
	public Contact() {
		super();
		this.contactId = -1;
		this.firstName = "";
		this.lastName = "";
		this.email = "";
		this.phoneNumber = "";
		this.address = "";
		this.comments = "";
	}

	/**
	 * Constructor accepting parameters.
	 * 
	 * @param contactId
	 *            The contact id of the Message.
	 * @param firstName
	 *            The first name of the contact.
	 * @param lastName
	 *            The last name of the contact.
	 * @param email
	 *            The contact's email.
	 * @param phoneNumber
	 *            The contact's phone number.
	 * @param address
	 *            The contact's address.
	 * @param comments
	 *            Comments.
	 */
	public Contact(int contactId, String firstName, String lastName,
			String email, String phoneNumber, String address, String comments) {
		super();
		this.contactId = contactId;
		this.firstName = firstName;
		this.lastName = lastName;
		this.email = email;
		this.phoneNumber = phoneNumber;
		this.address = address;
		this.comments = comments;
	}

	/**
	 * Returns the contact id.
	 * 
	 * @return The contact id.
	 */
	public int getContactId() {
		return contactId;
	}

	/**
	 * Sets the contact id.
	 * 
	 * @param contactId
	 *            The new contact id.
	 */
	public void setContactId(int contactId) {
		this.contactId = contactId;
	}

	/**
	 * Returns the contact's first name.
	 * 
	 * @return The first name.
	 */
	public String getFirstName() {
		return firstName;
	}

	/**
	 * Sets the contact's first name.
	 * 
	 * @param firstName
	 *            The contact's new first name.
	 */
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}

	/**
	 * Returns the contact's last name.
	 * 
	 * @return The last name.
	 */
	public String getLastName() {
		return lastName;
	}

	/**
	 * Sets the contact's last name.
	 * 
	 * @param lastName
	 *            The contact's new last name.
	 */
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}

	/**
	 * Returns the contact's email.
	 * 
	 * @return The email.
	 */
	public String getEmail() {
		return email;
	}

	/**
	 * Sets the contact's email.
	 * 
	 * @param email
	 *            The contact's new email.
	 */
	public void setEmail(String email) {
		this.email = email;
	}

	/**
	 * Returns the contact's phone number.
	 * 
	 * @return The phone number.
	 */
	public String getPhoneNumber() {
		return phoneNumber;
	}

	/**
	 * Sets the contact's phone number.
	 * 
	 * @param phoneNumber
	 *            The contacts's phone number.
	 */
	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}

	/**
	 * Returns the contact's address.
	 * 
	 * @return The contact's address.
	 */
	public String getAddress() {
		return address;
	}

	/**
	 * Sets the contact's address.
	 * 
	 * @param address
	 *            The new address.
	 */
	public void setAddress(String address) {
		this.address = address;
	}

	/**
	 * Returns the comments for this contact.
	 * 
	 * @return The comments.
	 */
	public String getComments() {
		return comments;
	}

	/**
	 * Sets the comments for this contact.
	 * 
	 * @param comments
	 *            The comments.
	 */
	public void setComments(String comments) {
		this.comments = comments;
	}

	/**
	 * Returns a String representation of the Contact object.
	 */
	@Override
	public String toString() {
		return "Contact [contactId=" + contactId + ", firstName=" + firstName
				+ ", lastName=" + lastName + ", email=" + email
				+ ", phoneNumber=" + phoneNumber + ", address=" + address
				+ ", comments=" + comments + "]";
	}
}
